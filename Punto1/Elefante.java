public class Elefante{
    private int Peso;
    private int ci;
    private int num;
 
    public Elefante(int Peso,int ci,int num){
        this.Peso = Peso;
        this.ci = ci;
        this.num = num;
    }
 
    public int getPeso(){
        return Peso;
    }
 
    public int getCi(){
        return ci;
    }
 
    public int getNum(){
        return num;
    }
    
 }